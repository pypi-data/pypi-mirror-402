"""
Script simple para comparar si ESTRELLA está contenido en VILLACRUZ
"""


def compare_regions(dict1, dict2, name1="ESTRELLA", name2="VILLACRUZ"):
    """Compara si dict1 está completamente contenido en dict2"""

    # Municipios faltantes
    municipios_faltantes = set(dict1.keys()) - set(dict2.keys())

    # Barrios faltantes por municipio
    barrios_faltantes = {}
    for municipio in dict1.keys():
        if municipio in dict2:
            faltantes = set(dict1[municipio]) - set(dict2[municipio])
            if faltantes:
                barrios_faltantes[municipio] = faltantes

    # Resultado
    esta_contenido = not municipios_faltantes and not barrios_faltantes

    print(f"\n¿{name1} está contenido en {name2}? {'✅ SÍ' if esta_contenido else '❌ NO'}\n")

    if municipios_faltantes:
        print(f"Municipios faltantes: {municipios_faltantes}\n")

    if barrios_faltantes:
        print("Barrios faltantes por municipio:")
        for municipio, barrios in barrios_faltantes.items():
            print(f"  {municipio}: {len(barrios)} barrios")
            for barrio in sorted(barrios):
                print(f"    - {barrio}")

    return esta_contenido


