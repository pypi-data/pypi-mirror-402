class MLSAcrecer(BaseBellatrix):
    __tablename__ = "mls_acrecer"
    id = Column(Integer, primary_key=True, autoincrement=True)
    code = Column(String(20), nullable=False)
    locationData = Column(MutableDict.as_mutable(MySQLJSON), nullable=False)
    agentPhone = Column(String(15), nullable=True)
    rooms = Column(MutableDict.as_mutable(MySQLJSON), nullable=True)
    privateZone = Column(Boolean, nullable=True)
    numberOfRooms = Column(Integer, nullable=True)
    features = Column(MutableDict.as_mutable(MySQLJSON), nullable=True)
    numberOf360Pictures = Column(Integer, nullable=True)
    householdFeatures = Column(MutableDict.as_mutable(MySQLJSON), nullable=True)
    propertyType = Column(String(50), nullable=True)
    onSale = Column(String(1), nullable=True)
    generalDescription = Column(Text, nullable=True)
    rentValue = Column(BigInteger, default=0)
    address = Column(String(255), nullable=True)
    readableAddress = Column(String(255), nullable=True)
    addedOn = Column(DateTime, nullable=False)
    propertyImages = Column(Text, nullable=True)
    stratum = Column(String(30), nullable=True)
    inCondominiumFeatures = Column(MutableDict.as_mutable(MySQLJSON), nullable=True)
    forRent = Column(String(1), nullable=True)
    availablePictures = Column(Integer, nullable=True)
    lastUpdate = Column(DateTime, nullable=False)
    constructionYears = Column(Integer, nullable=True)
    builtArea = Column(Integer, default=0.0)
    ciudad =Column(String(100), nullable=True)
    branchPhone = Column(String(30), nullable=True)
    branchName = Column(String(30), nullable=True)
    portalPhone = Column(String(30), nullable=True, default=True)
    propertyVideoUrl = Column(String(150), nullable=True)
    warehouseFeatures = Column(MutableDict.as_mutable(MySQLJSON), nullable=True)
    saleValue = Column(BigInteger, nullable=True)
    managementQuota = Column(BigInteger, nullable=True)
    landFeatures = Column(MutableDict.as_mutable(MySQLJSON), nullable=True)
    lat = Column(CHAR(50), nullable=True)
    lng = Column(CHAR(50), nullable=True)
    active = Column(Boolean, nullable=False, default=True)
    prefix_code= Column(String(10), nullable=True)
    source = Column(String(50), nullable=False)
