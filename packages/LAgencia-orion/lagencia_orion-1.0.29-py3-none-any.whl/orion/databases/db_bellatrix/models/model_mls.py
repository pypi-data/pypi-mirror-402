import datetime

from sqlalchemy import Boolean, Column, DateTime, Float, Integer, String, Text

from orion.databases.config_db_bellatrix import BaseBellatrix


class MLS(BaseBellatrix):
    __tablename__ = "mls"
    id = Column(Integer, primary_key=True)
    code = Column(Integer)
    street_name = Column(String(150), nullable=True)
    unit_number = Column(String(50), nullable=True)
    price_current = Column(Float)
    subdivision = Column(String(100), nullable=True)
    region = Column(String(100), nullable=True)
    district = Column(String(100), nullable=True)
    map_area = Column(String(100), nullable=True)
    property_type = Column(String(100), nullable=True)
    sqft_total = Column(String(100), nullable=True)
    lot_sqft = Column(String(100), nullable=True)
    lot_width_front = Column(String(100), nullable=True)
    lot_width_back = Column(String(100), nullable=True)
    lot_shape = Column(String(100), nullable=True)
    closed_area = Column(String(100), nullable=True)
    open_area = Column(String(100), nullable=True)
    water = Column(String(100), nullable=True)
    year_built = Column(Integer, nullable=True)
    construction = Column(String(100), nullable=True)
    possession = Column(String(100), nullable=True)
    title = Column(String(100), nullable=True)
    zoning = Column(String(100), nullable=True)
    remarks = Column(Text(collation="utf8mb4_general_ci"))  # Soporte para utf8mb4
    remarks_es = Column(Text(collation="utf8mb4_general_ci"))  # Soporte para utf8mb4
    interior_features = Column(Text, nullable=True)
    exterior_features = Column(Text, nullable=True)
    other_services = Column(Text, nullable=True)
    listing_office_id = Column(Integer, nullable=True)
    colisting_office_id = Column(String(100), nullable=True)
    listing_agent_id = Column(Integer, nullable=True)
    colisting_agent_id = Column(String(100), nullable=True)
    date_listed = Column(String(20), nullable=True)
    status = Column(String(20), nullable=True)
    unique_id = Column(String(10), nullable=True)
    listing_photo_count = Column(Integer, nullable=True)
    date_expired = Column(String(20), nullable=True)
    directions = Column(Text, nullable=True)
    directions_es = Column(Text, nullable=True)
    web_title = Column(Text(collation="utf8mb4_general_ci"))
    web_title_es = Column(Text(collation="utf8mb4_general_ci"))
    flooring = Column(String(100), nullable=True)
    roof = Column(String(100), nullable=True)
    remodelled = Column(String(100), nullable=True)
    access = Column(String(100), nullable=True)
    parking_spaces = Column(Integer, nullable=True)
    parking_level = Column(String(50), nullable=True)
    parking_types = Column(String(200), nullable=True)
    monthly_assessment = Column(String(20), nullable=True)
    assessment_includes = Column(Text, nullable=True)
    offices_x_floors = Column(Integer, nullable=True)
    number_of_buildings = Column(String(20), nullable=True)
    latitude = Column(String(20), nullable=True)
    longitude = Column(String(20), nullable=True)
    bedrooms = Column(Integer, nullable=True)
    bathrooms = Column(Integer, nullable=True)
    half_bathrooms = Column(Integer, nullable=True)
    number_of_rooms = Column(Integer, nullable=True)
    style = Column(Text, nullable=True)
    listing_agent_phone = Column(String(20), nullable=True)
    lot_features = Column(String(100), nullable=True)
    modification_date = Column(DateTime, default=datetime.datetime(1900, 1, 1, 0, 0, 0))
    internal_features = Column(Text, nullable=True)
    shared_amenities = Column(Text, nullable=True)
    taxes = Column(String(20), nullable=True)
    contract_type = Column(String(20), nullable=True)
    street_name_es = Column(String(150), nullable=True)
    show_livin = Column(Boolean, nullable=True)
    show_villacruz = Column(Boolean, nullable=True)
    show_castillo = Column(Boolean, nullable=True)
    show_estrella = Column(Boolean, nullable=True)
    active = Column(Boolean, nullable=False)
    date_insertion = Column(DateTime, nullable=True)
    management = Column(String(30))
    image = Column(Text)
    source = Column(String(50), nullable=False)
