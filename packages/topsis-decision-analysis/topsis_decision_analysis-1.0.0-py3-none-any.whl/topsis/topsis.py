import sys
import pandas as pd
import numpy as np


def read_input_file(input_file):
    """
    Read input file in CSV or Excel format.
    """
    try:
        return pd.read_csv(input_file)
    except Exception:
        try:
            return pd.read_excel(input_file)
        except Exception:
            print("Error: Input file must be a valid CSV or Excel file.")
            sys.exit(1)


def write_output_file(data, output_file):
    """
    Write output file in CSV or Excel format.
    """
    try:
        data.to_csv(output_file, index=False)
    except Exception:
        try:
            data.to_excel(output_file, index=False)
        except Exception:
            print("Error: Output file must be a valid CSV or Excel file.")
            sys.exit(1)


def topsis(input_file, weights, impacts, output_file):
    """
    Perform TOPSIS analysis on the given dataset.
    """
    # Read input data
    data = read_input_file(input_file)

    # Validate number of columns
    if data.shape[1] < 3:
        print("Error: Input file must contain at least three columns.")
        sys.exit(1)

    # Extract criteria columns (excluding first identifier column)
    criteria = data.iloc[:, 1:]

    try:
        criteria = criteria.astype(float)
    except ValueError:
        print("Error: All criteria values must be numeric.")
        sys.exit(1)

    # Parse weights and impacts
    weights = weights.split(',')
    impacts = impacts.split(',')

    if len(weights) != criteria.shape[1]:
        print("Error: Number of weights must match number of criteria.")
        sys.exit(1)

    if len(impacts) != criteria.shape[1]:
        print("Error: Number of impacts must match number of criteria.")
        sys.exit(1)

    if not all(impact in ['+', '-'] for impact in impacts):
        print("Error: Impacts must be either '+' or '-'.")
        sys.exit(1)

    weights = np.array(weights, dtype=float)

    # Normalize weights
    weights = weights / weights.sum()

    # Step 1: Normalize the decision matrix
    normalized_matrix = criteria / np.sqrt((criteria ** 2).sum())

    # Step 2: Apply weights
    weighted_matrix = normalized_matrix * weights

    # Step 3: Determine ideal best and ideal worst
    ideal_best = []
    ideal_worst = []

    for idx, impact in enumerate(impacts):
        column = weighted_matrix.iloc[:, idx]
        if impact == '+':
            ideal_best.append(column.max())
            ideal_worst.append(column.min())
        else:
            ideal_best.append(column.min())
            ideal_worst.append(column.max())

    ideal_best = np.array(ideal_best)
    ideal_worst = np.array(ideal_worst)

    # Step 4: Calculate separation measures
    distance_best = np.sqrt(((weighted_matrix - ideal_best) ** 2).sum(axis=1))
    distance_worst = np.sqrt(((weighted_matrix - ideal_worst) ** 2).sum(axis=1))

    # Step 5: Calculate TOPSIS score
    topsis_score = distance_worst / (distance_best + distance_worst + 1e-9)

    # Append results
    data['Topsis Score'] = topsis_score
    data['Rank'] = data['Topsis Score'].rank(ascending=False).astype(int)

    # Save output
    write_output_file(data, output_file)

    print("Result saved to", output_file)


def main():
    if len(sys.argv) != 5:
        print("Error: Incorrect number of parameters.")
        print("Usage: topsis <inputFile> <weights> <impacts> <outputFile>")
        sys.exit(1)

    topsis(sys.argv[1], sys.argv[2], sys.argv[3], sys.argv[4])


if __name__ == "__main__":
    main()