def check_surname(word: str) -> bool:
    """验证是否为中文姓名（兼顾准确率和性能）

    规则优先级：
    1. 排除黑名单词
    2. 长度2-4字
    3. 禁用后缀过滤
    4. 复姓优先匹配
    5. 单姓匹配
    """
    # 快速排除：成本最低的检查放在前面
    if word in BLACKLIST:
        return False
    if not 2 <= len(word) <= 4:
        return False
    if word[-1] in DISABLE_SUFFIX:
        return False

    # 复姓检测（优先长词匹配）
    if len(word) >= 3:
        # 先检查三字复姓（如 西門子 会被错误切分）
        # 注意：当前surnames集合中没有三字复姓，但保留逻辑以备扩展
        if word[:3] in surnames:
            return True

    if len(word) >= 2:
        # 匹配二字复姓（如欧阳、司马）
        if word[:2] in surnames:
            return True

    # 单姓检测
    if word[0] in surnames:
        return True

    return False


