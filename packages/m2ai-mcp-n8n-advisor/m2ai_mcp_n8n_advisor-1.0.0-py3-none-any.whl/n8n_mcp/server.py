"""n8n-mcp - MCP Server

Workflow status monitoring for n8n instances.

Generated by GRIMLOCK MCP Factory
"""

import asyncio
import logging
from typing import Any

from mcp.server import Server
from mcp.server.stdio import stdio_server
from mcp.types import TextContent, Tool

from .tools.check_workflows import check_workflows, CheckWorkflowsSchema

# Initialize logger
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger("n8n_mcp")

# Initialize MCP server
server = Server("n8n-mcp")


@server.list_tools()
async def list_tools() -> list[Tool]:
    """List available tools.

    Returns:
        List of Tool definitions for MCP protocol
    """
    return [
        Tool(
            name="check_workflows",
            description="Reviews all workflows and reports status. Shows total count, active/inactive breakdown, and identifies workflows with recent errors.",
            inputSchema=CheckWorkflowsSchema.model_json_schema(),
        ),
    ]


@server.call_tool()
async def call_tool(name: str, arguments: dict[str, Any]) -> list[TextContent]:
    """Handle tool calls.

    Args:
        name: Name of the tool to execute
        arguments: Tool arguments

    Returns:
        List of TextContent with result

    Raises:
        ValueError: If tool name is unknown
    """
    logger.info(f"Tool called: {name}", extra={"tool": name, "has_args": bool(arguments)})

    match name:
        case "check_workflows":
            return await check_workflows(arguments)
        case _:
            raise ValueError(f"Unknown tool: {name}")


async def run_server():
    """Run the MCP server using stdio transport."""
    logger.info("Starting n8n-mcp MCP server")

    async with stdio_server() as (read_stream, write_stream):
        await server.run(
            read_stream,
            write_stream,
            server.create_initialization_options(),
        )


def main():
    """Main entry point."""
    asyncio.run(run_server())


if __name__ == "__main__":
    main()
