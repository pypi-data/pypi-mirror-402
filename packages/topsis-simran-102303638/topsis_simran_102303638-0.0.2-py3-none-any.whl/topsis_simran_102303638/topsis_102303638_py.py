import sys
import pandas as pd
import numpy as np

def error(msg):
    print("Error:", msg)
    sys.exit(1)

def main():
    if len(sys.argv) != 5:
        error("Usage: python topsis.py <InputDataFile> <Weights> <Impacts> <OutputFileName>")

    input_file = sys.argv[1]
    weights_str = sys.argv[2]
    impacts_str = sys.argv[3]
    output_file = sys.argv[4]

    try:
        if input_file.endswith(".csv"):
            data = pd.read_csv(input_file)
        elif input_file.endswith(".xlsx") or input_file.endswith(".xls"):
            data = pd.read_excel(input_file)
        else:
            error("Only .csv or .xlsx files are supported.")
    except:
        error("File not found or cannot be opened.")

    if data.shape[1] < 3:
        error("Input file must contain three or more columns.")

    names = data.iloc[:, 0]
    matrix = data.iloc[:, 1:]

    try:
        matrix = matrix.astype(float)
    except:
        error("From 2nd to last columns must contain numeric values only.")

    weights = weights_str.split(",")
    impacts = impacts_str.split(",")

    if len(weights) != matrix.shape[1] or len(impacts) != matrix.shape[1]:
        error("Number of weights, impacts, and columns must be same.")

    try:
        weights = np.array(weights, dtype=float)
    except:
        error("Weights must be numeric and comma separated.")

    for i in impacts:
        if i not in ["+", "-"]:
            error("Impacts must be + or - only.")

    norm = np.sqrt((matrix**2).sum())
    norm_matrix = matrix / norm
    weighted = norm_matrix * weights

    ideal_best = []
    ideal_worst = []

    for j in range(weighted.shape[1]):
        if impacts[j] == "+":
            ideal_best.append(weighted.iloc[:, j].max())
            ideal_worst.append(weighted.iloc[:, j].min())
        else:
            ideal_best.append(weighted.iloc[:, j].min())
            ideal_worst.append(weighted.iloc[:, j].max())

    ideal_best = np.array(ideal_best)
    ideal_worst = np.array(ideal_worst)

    d_pos = np.sqrt(((weighted - ideal_best) ** 2).sum(axis=1))
    d_neg = np.sqrt(((weighted - ideal_worst) ** 2).sum(axis=1))

    score = d_neg / (d_pos + d_neg)
    rank = score.rank(ascending=False, method="dense")

    result = data.copy()
    result["Topsis Score"] = score
    result["Rank"] = rank.astype(int)

    result.to_csv(output_file, index=False)
    print("Result saved to", output_file)

if __name__ == "__main__":
    main()